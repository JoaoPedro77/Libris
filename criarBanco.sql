-- Criação do banco de dados
CREATE DATABASE IF NOT EXISTS biblioteca;
USE biblioteca;

-- Tabela de Categorias
CREATE TABLE IF NOT EXISTS categorias (
    id_categoria VARCHAR(10) PRIMARY KEY,
    nome VARCHAR(50) NOT NULL
);

-- Tabela de Livros
CREATE TABLE IF NOT EXISTS livros (
    id_livro VARCHAR(20) PRIMARY KEY,
    titulo VARCHAR(100) NOT NULL,
    autor VARCHAR(100) NOT NULL,
    isbn VARCHAR(20),
    ano_publicado INT,
    id_categoria VARCHAR(10),
    disponibilidade ENUM('Disponível', 'Indisponível') DEFAULT 'Disponível',
    FOREIGN KEY (id_categoria) REFERENCES categorias(id_categoria)
);

-- Tabela de Usuários
CREATE TABLE IF NOT EXISTS usuarios (
    matricula VARCHAR(20) PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    curso VARCHAR(50),
    telefone VARCHAR(20),
    email VARCHAR(100)
);

-- Tabela de Bibliotecários
CREATE TABLE IF NOT EXISTS bibliotecarios (
    login VARCHAR(50) PRIMARY KEY,
    senha VARCHAR(100) NOT NULL,
    nome VARCHAR(100) NOT NULL,
    matricula VARCHAR(20) NOT NULL UNIQUE
);

-- Tabela de Empréstimos
CREATE TABLE IF NOT EXISTS emprestimos (
    id INT AUTO_INCREMENT PRIMARY KEY,
    usuario VARCHAR(100) NOT NULL,
    livro VARCHAR(100) NOT NULL,
    livro_id VARCHAR(20) NOT NULL,
    matricula VARCHAR(20) NOT NULL,
    dataEmprestimo DATE NOT NULL,
    dataDevolucao DATE NOT NULL,
    dataDevolvido DATE NULL,
    multa DECIMAL(10,2) DEFAULT 0,
    status ENUM('ativo', 'atrasado', 'devolvido') DEFAULT 'ativo',
    FOREIGN KEY (livro_id) REFERENCES livros(id_livro),
    FOREIGN KEY (matricula) REFERENCES usuarios(matricula)
);

